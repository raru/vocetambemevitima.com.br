<h1>Agenda Emergencial pelo fim da Guerra às Drogas no Brasil</h1>
<center><strong><a href="#">english</a> | <a href="#">spanish</a> </strong></center>

<p><br/>
Chegou o momento da Plataforma Brasileira de Políticas sobre Drogas, a partir de sua rede de organizações membras, conselheiros consultivos e parceiros, e em parceria com a Iniciativa Negra por Uma Nova Política sobre Drogas, lançar ao mais amplo público a Agenda Emergencial pelo Fim da Guerra às Drogas no Brasil. <br/>
<br/>
Neste momento de disputa eleitoral das instituições democráticas no país, nos posicionamos pela interrupção imediata da opção de guerra que tem tido como consequências o super encarceramento de pessoas pretas e a criminalização de territórios empobrecidos do norte ao sul do Brasil. A guerra tem que parar já! <br/>
<br/>
Nos últimos meses conversamos com associações nacionais e internacionais da saúde, da segurança, da justiça, dos direitos humanos, do campo e da cidade; com os principais núcleos e centros de estudos científicos internacionalmente reconhecidos pelo seu acúmulo na temática de drogas e que estão presentes em universidades públicas e privadas do nosso país; com trabalhadoras e trabalhadores das redes de cuidado, justiça e garantia de direitos, que têm visto a política pública de atenção e cuidado ser completamente sucateada e desmontada; com movimentos, coletivos e lideranças, sobretudo mães de vítimas, residentes dos territórios mais atingidos pela opção bárbara, ineficaz e insustentável da guerra. Apresentamos neste texto-manifesto uma agenda orgânica, que é fruto de uma longa pactuação com esses diferentes setores sociais mobilizados pela nossa rede.<br/>
<br/>
Nós somos pessoas que usam drogas, cientistas e acadêmicas, trabalhadoras de serviços, movimentos sociais, pessoas em situação de rua, coletivos culturais, e integramos um campo político com corações solidários e mentes brilhantes que ousam imaginar, experimentar, formular e acompanhar as mudanças das políticas sobre drogas no Brasil e no mundo há muitos anos.<br/>
<br/>
Na guerra às drogas, você também é vítima! A opção político ideológica da proibição de algumas drogas resultou em uma forte indústria internacional ilegal, que ocorre sem nenhuma regulamentação, sobrevivendo mediante grandes esquemas de corrupção financeira de instituições públicas para manutenção de seus negócios. Ela não tem nenhum apreço ou cuidado com a vida das pessoas, sobretudo àquelas mais pobres e vulneráveis, impactadas pelas inúmeras violências provocadas por essa política. <br/>
<br/>
Além dessas ameaças causadas pelas atividades criminosas, ao optar pela manutenção da guerra às drogas, direcionamos o investimento do orçamento público da União, de Estados e Municípios para políticas ineficazes em áreas estratégicas como Justiça, Segurança, Saúde e Assistência Social, que impactam também na sua vida, independente se você usa ou não drogas.<br/>
<br/>
É preciso desinterditar o debate sobre as consequências desta opção e recompor o investimento público de modo a resolver no curto, médio e longo prazo a questão da produção, da circulação e do consumo de substâncias a partir de boas práticas sociais, ambientais, de saúde e justiça, explorando as experiências comprovadamente recomendadas na América Latina e no mundo.<br/>
<br/>
É preciso interromper a agenda de morte financiada pela Guerra às Drogas, direcionando os esforços públicos para uma agenda de promoção de direitos capaz de reverter as desigualdades produzidas por décadas de guerra. Essa política não é mais uma opção para nós. A Democracia está em risco e não podemos e nem queremos mais ser as vítimas que pagam a conta da atual política de drogas, pois investir na guerra significa não optar por comida, educação pública e de qualidade, investimento em ideias criativas, cultura, geração de emprego e renda etc. <br/>
<br/>
É por isso que viemos a público dizer que #VocêTambémÉVítima da opção de guerra às drogas que tem trazido consequências a diferentes camadas da população em todo mundo. <br/>
<br/>
Apresentamos abaixo, em quatro eixos estruturais, a nossa Agenda Emergencial. Essa agenda é parte integrante da Caravana Nacional do Conhecimento, evento capitaneado pelas entidades membras da Plataforma e que está ocorrendo em todas as regiões do país, até o fim de 2022, em um amplo processo de escuta de especialistas, trabalhadoras de serviços e movimentos sociais que deverão subsidiar a produção de uma Agenda Emergente para a Políticas sobre Drogas no Brasil, cujo lançamento está previsto para o início de 2023. Cola com a gente!<br/>
<br/>
<br/>
</p>
<h2 style="color:#493063;" id="eixodocuidado">EIXO DO CUIDADO</h2>
<p>
Acreditamos em práticas de cuidado, a partir de diretrizes técnico-políticas baseadas em evidências científicas que estejam alinhadas aos princípios de uma atuação territorial e comunitária, de respeito aos direitos humanos, do cuidado em liberdade pautado na ética da redução de danos, na laicidade do Estado e na oferta integral de serviços e oportunidades. Para isso, propomos:<br/>
<br/>
1 - Desenvolvimento de programas de promoção de saúde, prevenção e educação comunitária comprometidos com a disseminação de informações e práticas que diminuam os danos associados ao uso de álcool e outras drogas;<br/>
<br/>
2 - Fortalecimento e ampliação de investimentos nos serviços oferecidos pelo Sistema Único de Saúde e Sistema Único de Assistência Social nas áreas de acolhimento, cuidado e redução de danos relacionados a pessoas que usam álcool e outras drogas;<br/>
<br/>
3 - Garantia de investimento continuado em formação/supervisão de práticas para trabalhadoras dos serviços de saúde e assistência social, assim como de novo investimentos visando o fortalecimento de espaços para articulação territorial de serviços e comunidades;<br/>
<br/>
4 - Programas e práticas intersetoriais que integrem determinantes sociais em saúde com o  cuidado multidisciplinar, como soluções em moradia e geração de renda destinadas a pessoas de alto grau de vulnerabilidade associado ao uso de álcool e outras drogas.<br/>

<br/>
<br/>
</p>
<h2 style="color:#0a9888;" id="eixojusticaeseguranca">EIXO JUSTIÇA E SEGURANÇA</h2>
<p>
São muitas as mazelas que a guerras às drogas opera pelo campo da Justiça e Segurança Pública: chacinas, encarceramento em massa, homicídios sistemáticos, corrupção, criminalização das pessoas que usam drogas, sobretudo as negras e pobres, militarização de territórios e repressão desproporcional ao comércio e consumo de substâncias tornadas ilícitas. Nesta guerra todas e todos nós perdemos. Apresentamos outros caminhos possíveis por meio das seguintes propostas: <br/>
<br/>
<br/>
1 - Anistia para presos provisórios por delitos relacionados a drogas que muitas vezes foram provados de Liberdade em decorrência do porte de uma baixíssima quantidade de substâncias consideradas ilícitas;<br/>
<br/>
2 - Amplo investimento em programas destinados a integração de pessoas egressas do sistema prisional, priorizando geração de renda, assistência jurídica e acesso a políticas sociais diversas;<br/>
<br/>
3 - Desenvolvimento de ações comunitárias que ampliem a fiscalização de ações das polícias e acompanhamento de investigações de casos junto a órgãos externos às polícias;<br/>
<br/>
4 - Fortalecimento da Defensoria Pública e de comissões externas para proposição, monitoramento e fiscalização das atividades desenvolvidas pelas instituições de Segurança e Justiça, com ampla participação da sociedade civil.<br/>
<br/>
<br/>
</p>
<h2 style="color:#e74763;" id="eixoregularparareparar">EIXO REGULAR PARA REPARAR</h2>
<p>
Diante do movimento global de diversos países que já estão desenvolvendo novos marcos regulatórios para substâncias (especialmente a cânabis, mas não apenas) e de reparação para populações que vivem em territórios diretamente afetado pela guerra às drogas, nós propomos:<br/>
<br/>
1 - Revisão imediata da atual legislação de drogas no Brasil visando a regulação em caráter de urgência das substâncias e permitindo a tributação e o monitoramento eficaz da sua produção, do seu comércio e do seu consumo;<br/>
<br/>
2 - Criação de fundo, derivado de recursos financeiros da tributação, para a reparação de populações que habitam territórios afetados diretamente pela guerra às drogas, de forma a garantir recursos no campo da saúde, assistência, educação, moradia e outras, que sirvam como forma de indenização aliadas à redução de desigualdades;<br/>
<br/>
3 - Discussão do modelo de produção de cannabis no Brasil, posicionando o fértil território nacional para cultivo da planta de forma a contemplar iniciativas e interesses públicos e privados, garantindo, sobretudo, que a tributação e criação da cadeia produtiva deste mercado gere um impacto estrutural na arrecadação e no campo produtivo do país, fortalecendo os arranjos produtivos locais e a agricultura orgânica familiar;<br/>
<br/>
4 - Fomento de pesquisas em universidades públicas relacionadas à implantação e avaliação dos impactos de nova legislação sobre drogas, de forma a ter um monitoramento e uma avaliação sistematizada, cientificamente resguardada, capaz de medir a eficácia desta nova opção a curto, médio e longo prazo.<br/>
<br/>
<br/>
</p>
<h2 style="color:#2b6a4b;" id="eixoparticipacaoecontrolesocial">EIXO PARTICIPAÇÃO E CONTROLE SOCIAL</h2>
<p>
As múltiplas ações capazes de fortalecer a formação, execução, fiscalização e avaliação de políticas relacionadas às drogas devem ter ampla participação e controle de setores sociais, como as pessoas que usam substâncias, trabalhadores de serviços de saúde, assistência social, justiça, organizações da sociedade civil entre outras, a fim de direcionar essas políticas ao interesse público.<br/>
<br/>
1 - Reconfiguração do Conselho Nacional de Políticas Sobre Drogas (CONAD), tornando-o paritário entre sociedade civil e representações institucionais do Estado, conferindo poder efetivo para monitoramento, proposição e controle social sobre das políticas de drogas, inclusive no que tange às decisões orçamentárias;<br/>
<br/>
2 - Realização da I Conferência Nacional de Políticas Sobre Drogas precedida de etapas estaduais e municipais, permitindo que as populações mais afetadas por estas políticas possam tomar o protagonismo de sua formulação e de seu monitoramento;<br/>
<br/>
3 - Fortalecimento da Secretaria Nacional de Políticas sobre Drogas, posicionando-a na estrutura administrativa de forma a garantir a intersetorialidade da formulação, execução e monitoramento desta política.<br/>
<br/>
